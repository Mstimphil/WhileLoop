package com.pluralsight;

public class While {
    public static void main(String[] args) {
        System.out.println("I love Java");
        System.out.println("I love Java");
        System.out.println("I love Java");
        System.out.println("I love Java");
        System.out.println("I love Java");

        String greeting = "I love Java";

        while (greeting == 5) {
            System.out.println(greeting);
        }

    }
}
